/*   subroutine to sort out read pairs    */
/* =============================== */
void Matrix_Process(char **argv,int args,int nSeq)
/* =============================== */
{
     int i,j,k,m,n,idt,st,ed,n_contigs,num_sets,n_cells;
     FILE *namef,*namef2;
     int **rmatrix;
     char rdname[60];
     int stopflag,offset,ctg_len,add_len,n_scaffs;
     int **imatrix(long nrl,long nrh,long ncl,long nch);


     rmatrix=imatrix(0,100,0,100);

     if((namef2 = fopen(argv[args+1],"w")) == NULL)
     {
       printf("ERROR main:: reads group file \n");
       exit(1);
     }

     for(i=0;i<nSeq;i++)
     {
/*      search reads with an index < i     */
/*      search reads with an index > i     */
        stopflag=0;
        j=i+1;
        while((j<nSeq)&&(stopflag==0))
        {
          if((hit_locus1[i] == hit_locus1[j])&&(hit_locus2[i] == hit_locus2[j]))
          {
            j++;
          }
          else
            stopflag=1;
        }
        if((j-i)>=2)
        {
           int num_hits = j-i;
           int NK = 0;
           int MK = 0;
//        printf("www: %d %d %d %s %s\n",i,hit_locus1[i],hit_locus2[i+1],Samname[i],Samname[i+1]);
           for(n=0;n<num_hits;n++)
           {
              for(m=0;m<num_hits;m++)
              {
		if(n != m)
		{
                  for(k=0;k<9;k++)
		  {
		    if(strcmp(Comname[k],Samname[n+i]) == 0)
		    {
		       NK = k;
		       k = 9;
		    }
		  }
                  for(k=0;k<9;k++)
		  {
		    if(strcmp(Comname[k],Samname[m+i]) == 0)
		    {
		       MK = k;
		       k = 9;
		    }
		  }
//        printf("kkk: %d %d %d %s %s\n",i,NK,MK,Comname[NK],Comname[MK]);
                  rmatrix[NK][MK]++;
                  rmatrix[MK][NK]++;
		}
              }
           }
        }
        else
        {
        }
        i=j-1;
     }
     fclose(namef2);
     printf("            %s %s %s %s %s %s %s %s %s\n",Comname[0],Comname[1],Comname[2],Comname[3],Comname[4],Comname[5],Comname[6],Comname[7],Comname[8]);
     for(k=0;k<9;k++)
        printf("%s %8d %8d %8d %8d %8d %8d %8d %8d %8d\n",Comname[k],rmatrix[k][0],rmatrix[k][1],rmatrix[k][2],rmatrix[k][3],rmatrix[k][4],rmatrix[k][5],rmatrix[k][6],rmatrix[k][7],rmatrix[k][8]);
}


#define SWAP(a,b) temp=(a);(a)=b;(b)=temp;

/*   Subroutine to sort an array arr[0,...,n-1] into ascending order while
     making the corresponding reaarangement of the array brr[0,...,n-1]
     by the use of Quicksort (Sedgwick, R. 1978, Communications o fthe ACM,
     vol. 21, pp. 847-857) also see Numerical Recipes in C                  */  

/* =============================== */
void ArraySort_Long(int n, long *arr)
/* =============================== */
{
     int i,ir=n-1,j,k,m=0,jstack=0,NSTACK=50,istack[NSTACK];
     long a,temp,MIN=7;

     for(;;)
     {
/*      Insertion sort when subarray is small enough    */
        if(ir-m<MIN)
        {
          for(j=m+1;j<=ir;j++)
          {
             a=arr[j];
             for(i=j-1;i>=m;i--)
             {
                if(arr[i]<=a) break;
                arr[i+1]=arr[i];
             }
             arr[i+1]=a;
          }
          if(!jstack) return;
          ir=istack[jstack--];
          m=istack[jstack--];
        }
        else
        {
          k=(m+ir)>>1;
          SWAP(arr[k],arr[m+1]);

          if(arr[m]>arr[ir])
          {
            SWAP(arr[m],arr[ir]);
          }

          if(arr[m+1]>arr[ir])
          {
            SWAP(arr[m+1],arr[ir]);
          }

          if(arr[m]>arr[m+1])
          {
            SWAP(arr[m],arr[m+1]);
          }

          i=m+1;
          j=ir;
          a=arr[m+1];
          for(;;)
          {
             do i++; while (arr[i]<a);
             do j--; while (arr[j]>a);
             if(j<i) break;
             SWAP(arr[i],arr[j]);
          }
          arr[m+1]=arr[j];
          arr[j]=a;
          jstack+=2;

/*        Push pointers to larger subarray on stack      */
/*        process smaller subarray immediately           */
          if(jstack>NSTACK)
          {
             printf("Stack error: NSTACK too small\n");
             exit(0);
          }
          if(ir-i+1>=j-m)
          {
            istack[jstack]=ir;
            istack[jstack-1]=i;
            ir=j-1;
          }
          else
          {
            istack[jstack]=j-1;
            istack[jstack-1]=m;
            m=i;
          }
        }
     }
}


